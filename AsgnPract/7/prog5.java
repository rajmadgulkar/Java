
import java.util.*;

class P5{
	public static void main(String[]args){
		Scanner sc=new Scanner(System.in);
		System.out.print("Enter day:");
		int day=sc.nextInt();
		int i=day;
		while(i>=1){

			System.out.println(i+" days remaining");
			i--;




		}
		System.out.println(i+" days remaining is Overdue");






	}





}
